/* USER CODE BEGIN 0 */
#define LCD_ADDR (0x27 << 1)   // đổi thành 0x3F<<1 nếu module là 3F

// Mapping loại 4 (đã xác nhận bằng PulseView)
#define RS (1<<0)
#define RW (1<<1)
#define EN (1<<2)
#define BL (1<<3)

void lcd_send_cmd(uint8_t cmd)
{
    uint8_t u = cmd & 0xF0;        // high nibble
    uint8_t l = (cmd << 4) & 0xF0; // low nibble

    uint8_t data[4] = {
        (u) | BL | EN,   // EN=1, RS=0
        (u) | BL,        // EN=0
        (l) | BL | EN,
        (l) | BL
    };

    HAL_I2C_Master_Transmit(&hi2c1, LCD_ADDR, data, 4, 100);
}

void lcd_send_data(uint8_t data_in)
{
    uint8_t u = data_in & 0xF0;
    uint8_t l = (data_in << 4) & 0xF0;

    uint8_t data[4] = {
        (u) | BL | EN | RS,   // EN=1, RS=1
        (u) | BL | RS,        // EN=0
        (l) | BL | EN | RS,
        (l) | BL | RS
    };

    HAL_I2C_Master_Transmit(&hi2c1, LCD_ADDR, data, 4, 100);
}

void lcd_init(void)
{
    HAL_Delay(50);

    lcd_send_cmd(0x30);
    HAL_Delay(5);
    lcd_send_cmd(0x30);
    HAL_Delay(1);
    lcd_send_cmd(0x30);
    HAL_Delay(10);

    lcd_send_cmd(0x20); // 4-bit mode

    lcd_send_cmd(0x28); // 2 lines, 5x8 font
    lcd_send_cmd(0x0C); // display ON, cursor OFF
    lcd_send_cmd(0x01); // clear
    HAL_Delay(2);
    lcd_send_cmd(0x06); // entry mode
}

void lcd_puts(char *str)
{
    while (*str)
        lcd_send_data(*str++);
}

